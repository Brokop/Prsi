package com.gasimo;

/**
 * Defines Possible actions made by the player
 */
public enum TurnActions {

    /**
     * Pickup an card from deck
     */
    PICK,


    /**
     * Place an card
     */
    PLACE,

    /**
     * Change Color using Svrsek
     */
    CHANGE_COLOR,

    /**
     * Skip a turn (ESO)
     */
    SKIP

}
